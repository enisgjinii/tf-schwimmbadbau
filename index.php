  <script>
  document.addEventListener('DOMContentLoaded', function() {
    const carousel = document.getElementById('heroCarousel');
    const carouselItems = carousel.querySelectorAll('.carousel-item');
    
    // Function to animate text elements
    function animateTextElements(container) {
      const elements = container.querySelectorAll('h1, p, .d-flex');
      elements.forEach((element, index) => {
        element.style.opacity = '0';
        element.style.transform = 'translateY(30px)';
        
        setTimeout(() => {
          element.style.transition = 'all 0.8s ease';
          element.style.opacity = '1';
          element.style.transform = 'translateY(0)';
        }, 100 + (index * 200));
      });
    }
    
    // Function to reset text elements
    function resetTextElements(container) {
      const elements = container.querySelectorAll('h1, p, .d-flex');
      elements.forEach(element => {
        element.style.opacity = '0';
        element.style.transform = 'translateY(30px)';
        element.style.transition = 'none';
      });
    }
    
    // Animate initial slide
    animateTextElements(carouselItems[0]);
    
    // Listen for slide changes
    carousel.addEventListener('slid.bs.carousel', function(event) {
      const activeItem = carouselItems[event.to];
      const previousItem = carouselItems[event.from];
      
      // Reset previous slide
      resetTextElements(previousItem);
      
      // Animate new slide
      setTimeout(() => {
        animateTextElements(activeItem);
      }, 100);
    });
  });
  </script>
